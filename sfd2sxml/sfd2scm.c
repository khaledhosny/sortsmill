#include <stdio.h>
#include <progname.h>
#include <sfd_read.h>

int
main (int argc, char **argv)
{
  set_program_name (argv[0]);

  FILE *f = fopen (argv[1], "r");
  if (f != NULL)
    {
      sfd_to_scheme (get_sfd_line_from_file, f);
    }

  return 0;
}

